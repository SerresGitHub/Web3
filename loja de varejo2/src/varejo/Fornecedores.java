package varejo;

public class Fornecedores {

	private String nomeDoFornecedores;
	private String numeroDoFornecedor;
	private String ruaDoFornecedor;
	private String bairroDoFornecedor;
	private String cidadeDoFornecedor;
	private String estadoDoFornecedor;
	
	

	
	public String getNomeDoFornecedores() {
		return nomeDoFornecedores;
	}
	public String getNumeroDoFornecedor() {
		return numeroDoFornecedor;
	}
	public String getRuaDoFornecedor() {
		return ruaDoFornecedor;
	}
	public String getBairroDoFornecedor() {
		return bairroDoFornecedor;
	}
	public String getCidadeDoFornecedor() {
		return cidadeDoFornecedor;
	}
	public String getEstadoDoFornecedor() {
		return estadoDoFornecedor;
	}
	public void setNomeDoFornecedores(String nomeDoFornecedores) {
		this.nomeDoFornecedores = nomeDoFornecedores;
	}
	public void setNumeroDoFornecedor(String numeroDoFornecedor) {
		this.numeroDoFornecedor = numeroDoFornecedor;
	}
	public void setRuaDoFornecedor(String ruaDoFornecedor) {
		this.ruaDoFornecedor = ruaDoFornecedor;
	}
	public void setBairroDoFornecedor(String bairroDoFornecedor) {
		this.bairroDoFornecedor = bairroDoFornecedor;
	}
	public void setCidadeDoFornecedor(String cidadeDoFornecedor) {
		this.cidadeDoFornecedor = cidadeDoFornecedor;
	}
	public void setEstadoDoFornecedor(String estadoDoFornecedor) {
		this.estadoDoFornecedor = estadoDoFornecedor;
	}
	
	
	
}